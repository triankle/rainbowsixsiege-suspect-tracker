(function initR6ReportUtils(global) {
  function buildShareableReport(payload) {
    const reasons = Array.isArray(payload.reasons)
      ? payload.reasons.map((r) => `- ${r.text || String(r)}`).join('\n')
      : '- No detailed reasons';

    return `R6 Suspect Check Report
Pseudo: ${payload.pseudo || '—'}
Verdict: ${payload.verdictLabel}
Classification: ${payload.classification}
Confidence: ${payload.confidence}
K/D: ${payload.kd}
Win rate: ${payload.winrate == null ? '—' : `${payload.winrate}%`}
Ranked matches: ${payload.ranked}
Account level: ${payload.level}
Rank: ${payload.rankKey || '—'}
Seasons: ${payload.playedSeasons.join(', ')}
Cheat score: ${Math.round(payload.cheatScore)}%
Smurf score: ${Math.round(payload.smurfScore)}%
Final suspicion: ${Math.round(payload.finalScore)}/100

Reasons:
${reasons}

Generated by R6 Suspect Check. This is an indicator, not proof.`;
  }

  global.R6Report = {
    buildShareableReport,
  };
})(window);
